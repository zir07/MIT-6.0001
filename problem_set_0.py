first_number=int(input("Enter the first number: "))
second_number=int(input("Enter the second number: "))
import math
power=(first_number**second_number)
print(f'{first_number} raised to {second_number} is {power}')
logarithm=(math.log2(first_number))
print(f'The base 2 log of {first_number} is {logarithm}')